# Método número 1 para resolver o Desafio 1 - Algoritmo Peak Finding

print('Valores de pico de cada lista e seu respectivo índice:')

list_one = [1, 2, 3]
max_value = max(list_one)
print(
    f'\nMaior valor da lista 1: {max_value}\nÍndice: {list_one.index(max_value)}')

list_two = [2, 3, 1]
max_value2 = max(list_two)
print(
    f'\nMaior valor da lista 2: {max_value2}\nÍndice: {list_two.index(max_value2)}')

list_three = [3, 2, 1]
max_value3 = max(list_three)
print(
    f'\nMaior valor da lista 3: {max_value3}\nÍndice: {list_three.index(max_value3)}')

list_four = [1, 2, 3, 1, 5]
max_value4 = max(list_four)
print(
    f'\nMaior valor da lista 4: {max_value4}\nÍndice: {list_four.index(max_value4)}')
